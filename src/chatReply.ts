/**
 * 离线/请求失败时的兜底回复（与线上模型话术一致，不出现「未接入」类提示）。
 */
export function buildLocalReply(userText: string): string {
  const t = userText.trim()
  if (!t) {
    return '请输入您想处理的任务，或从下方快捷指令开始。'
  }

  const rules: { test: (s: string) => boolean; reply: string }[] = [
    {
      test: (s) => /选题|爆款/.test(s),
      reply:
        '【爆款选题 · 陈科豪体系】\n请补充：赛道/人设、目标人群、近期热点或产品卖点。我将按人设校验与情绪标注，为您批量输出选题清单（含钩子方向与拍摄建议）。',
    },
    {
      test: (s) => /脚本|口播/.test(s),
      reply:
        '【口播脚本】\n请提供：时长（如 60s）、核心信息点、语气风格。我将输出带情绪曲线与注意力管理的完整脚本（含分段停顿提示）。',
    },
    {
      test: (s) => /开头|开篇|前3|钩子/.test(s),
      reply:
        '【开头优化 · 13 种钩子】\n请粘贴当前开头文案或简述视频主题。我将给出 3–5 个改写版本，并标注适用的钩子类型与留存预期。',
    },
    {
      test: (s) => /账号|诊断|策划/.test(s),
      reply:
        '【账号诊断】\n请提供：账号链接或主页截图说明、近 10 条内容主题、当前变现方式。我将从价值/用户/人设/类型/风格五维给出定位与内容线规划。',
    },
    {
      test: (s) => /拆|分析.*视频|爆款视频/.test(s),
      reply:
        '【爆款拆解】\n请发视频链接或描述结构（开头-中段-结尾）。我将拆解动力结构并输出可复用模板与迁移到你的赛道的建议。',
    },
    {
      test: (s) => /复盘|数据/.test(s),
      reply:
        '【内容复盘】\n请提供：单条或周期数据（播放、完播、互动）、内容主题与投放/发布时间。我将做归因并给出下一轮迭代动作清单。',
    },
    {
      test: (s) => /朋友圈|上新|活动/.test(s),
      reply:
        '【朋友圈 20 条 · 四阶段】\n请说明：产品/活动、卖点、活动时间线、希望调性。我将按「埋种子→塑价值→造期待→引爆发」输出共 20 条文案草案。',
    },
    {
      test: (s) => /搜索|查一下|检索|资讯|联网/.test(s),
      reply:
        '【联网检索】\n请打开「联网搜索」或点「热点选题 / 随便问问」等带联网的快捷指令，并写明你想查的主题与用途（如：行业动态、对标案例、数据出处）。',
    },
    {
      test: (s) => /python|node|代码|执行/.test(s),
      reply:
        '【代码相关】\n请粘贴代码与运行环境（Python 或 Node）、期望输出。我会逐行说明逻辑、潜在风险与改法；若需在本机运行，请仅在可信环境下执行。',
    },
  ]

  for (const { test, reply } of rules) {
    if (test(t)) return reply
  }

  return `收到：「${t}」\n\n我可以在短视频运营、朋友圈营销、选题脚本与联网检索等方面协助你。请补充赛道、人设、时长或具体目标，也可以直接点快捷指令开聊。\n\n若刚才请求失败，多半是网络或服务暂时不可用，请稍后再试。`
}
